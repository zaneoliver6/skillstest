     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'name'      => 'required|string|max:255',
            "quantity"  => 'required|integer',
            "price"     => 'required|integer'
        ]);

        $input = $request->input();
        $jsonData = \json_encode($input);

        if(file_put_contents('products.json', $jsonData,FILE_APPEND)){
            if($request->ajax()) {
                return response()->json(['status' => 'OK','products' => $jsonData]);
            }
        } else {
            return response()->json(['status' => 'BAD','message' => 'cannot write to file']);
        }
    }

